// Dizilere veri eklemek için push() ve unshift() metodlarını kullanabiliriz.
// push (), verileri dizinin sonuna ekler.
// unshift(), verileri dizinin başına ekler.

let twentyThree = "XXIII";
let romanNumerals = ["XXI", "XXII"];
romanNumerals.unshift("XIX","XX"); // dizinin başına ekledi
romanNumerals.push(twentyThree); // dizinin sonuna ekledi.
